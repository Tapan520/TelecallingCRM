using System.Text;
using System.Threading.RateLimiting;
using Hangfire;
using Hangfire.MySql;
using Microsoft.AspNetCore.Authentication.JwtBearer;
using Microsoft.AspNetCore.Identity;
using Microsoft.EntityFrameworkCore;
using Microsoft.IdentityModel.Tokens;
using Serilog;
using Serilog.Events;
using TelecallingCRM.Api;
using TelecallingCRM.Data;
using TelecallingCRM.Data.Models;
using TelecallingCRM.Hubs;
using TelecallingCRM.Services;

// ?? Serilog bootstrap ?????????????????????????????????????????????????????????
Log.Logger = new LoggerConfiguration()
    .MinimumLevel.Information()
    .MinimumLevel.Override("Microsoft.EntityFrameworkCore.Database.Command", LogEventLevel.Warning)
    .WriteTo.Console(outputTemplate: "[{Timestamp:HH:mm:ss} {Level:u3}] {Message:lj}{NewLine}{Exception}")
    .WriteTo.File("logs/crm-.log", rollingInterval: RollingInterval.Day, retainedFileCountLimit: 14)
    .CreateLogger();

var builder = WebApplication.CreateBuilder(args);
builder.Host.UseSerilog();

// ?? Database ??????????????????????????????????????????????????????????????????
var connStr = builder.Configuration.GetConnectionString("DefaultConnection")
    ?? throw new InvalidOperationException("DefaultConnection not configured");

builder.Services.AddDbContext<AppDbContext>(options =>
    options.UseMySql(connStr, new MySqlServerVersion(new Version(8, 0, 0))));

// ?? Identity ??????????????????????????????????????????????????????????????????
builder.Services.AddIdentity<AppUser, IdentityRole<Guid>>(options =>
    {
        options.Password.RequireDigit = true;
        options.Password.RequiredLength = 8;
        options.Password.RequireNonAlphanumeric = false;
        options.SignIn.RequireConfirmedAccount = false;
        options.Lockout.DefaultLockoutTimeSpan = TimeSpan.FromMinutes(5);
        options.Lockout.MaxFailedAccessAttempts = 5;
        options.Lockout.AllowedForNewUsers = true;
    })
    .AddEntityFrameworkStores<AppDbContext>()
    .AddDefaultTokenProviders();

builder.Services.ConfigureApplicationCookie(options =>
{
    options.LoginPath = "/Login";
    options.LogoutPath = "/Logout";
    options.AccessDeniedPath = "/AccessDenied";

    // For /api/* requests, return 401/403 status codes directly instead of
    // redirecting to the login/access-denied pages. Without this, the browser
    // fetch() follows the 302 redirect, receives HTML, and res.json() throws.
    options.Events.OnRedirectToLogin = ctx =>
    {
        if (ctx.Request.Path.StartsWithSegments("/api"))
        {
            ctx.Response.StatusCode = StatusCodes.Status401Unauthorized;
            return Task.CompletedTask;
        }
        ctx.Response.Redirect(ctx.RedirectUri);
        return Task.CompletedTask;
    };
    options.Events.OnRedirectToAccessDenied = ctx =>
    {
        if (ctx.Request.Path.StartsWithSegments("/api"))
        {
            ctx.Response.StatusCode = StatusCodes.Status403Forbidden;
            return Task.CompletedTask;
        }
        ctx.Response.Redirect(ctx.RedirectUri);
        return Task.CompletedTask;
    };
});

// ?? JWT ???????????????????????????????????????????????????????????????????????
var jwtSecret = builder.Configuration["Jwt:Secret"]
    ?? throw new InvalidOperationException("Jwt:Secret not configured");

builder.Services.AddAuthentication()
    .AddJwtBearer(JwtBearerDefaults.AuthenticationScheme, options =>
    {
        options.TokenValidationParameters = new TokenValidationParameters
        {
            ValidateIssuer = true, ValidIssuer = builder.Configuration["Jwt:Issuer"],
            ValidateAudience = true, ValidAudience = builder.Configuration["Jwt:Audience"],
            ValidateIssuerSigningKey = true,
            IssuerSigningKey = new SymmetricSecurityKey(Encoding.UTF8.GetBytes(jwtSecret)),
            ValidateLifetime = true
        };
    });

builder.Services.AddAuthorization();

// ?? Rate Limiting (built-in ASP.NET Core 8) ???????????????????????????????????
builder.Services.AddRateLimiter(opt =>
{
    opt.AddPolicy<string, LoginRateLimitPolicy>("login");
    opt.AddPolicy<string, ApiRateLimitPolicy>("api");
    opt.RejectionStatusCode = 429;
});

// ?? Hangfire ??????????????????????????????????????????????????????????????????
builder.Services.AddHangfire(config =>
    config.UseStorage(new MySqlStorage(connStr, new MySqlStorageOptions
    {
        TablesPrefix = "Hangfire_",
        PrepareSchemaIfNecessary = true
    })));
builder.Services.AddHangfireServer(opt => opt.WorkerCount = 4);

// ?? Multi-tenancy ?????????????????????????????????????????????????????????????
builder.Services.AddScoped<TenantContext>();
builder.Services.AddScoped<ITenantResolver, TenantResolver>();
builder.Services.AddScoped<IUserClaimsPrincipalFactory<AppUser>, AppUserClaimsPrincipalFactory>();

// ?? Application services ??????????????????????????????????????????????????????
builder.Services.AddScoped<ITokenService, TokenService>();
builder.Services.AddScoped<IOpenRouterService, OpenRouterService>();
builder.Services.AddScoped<IWhisperService, WhisperService>();
builder.Services.AddScoped<IKnowledgeService, KnowledgeService>();
builder.Services.AddScoped<IMessageDispatcher, MessageDispatcher>();
builder.Services.AddScoped<IWebhookDispatcher, WebhookDispatcher>();
builder.Services.AddScoped<ICallAiProcessor, CallAiProcessor>();
builder.Services.AddScoped<ScheduledJobService>();

// ?? HTTP clients ??????????????????????????????????????????????????????????????
builder.Services.AddHttpClient("openrouter");
builder.Services.AddHttpClient("whisper");
builder.Services.AddHttpClient();

// ?? SignalR + Razor Pages + Health ????????????????????????????????????????????
builder.Services.AddMemoryCache();
builder.Services.AddSignalR();
builder.Services.AddRazorPages();
builder.Services.AddHealthChecks()
    .AddDbContextCheck<AppDbContext>("database");

var app = builder.Build();

// ?? Auto-migrate + seed ???????????????????????????????????????????????????????
using (var scope = app.Services.CreateScope())
{
    var db = scope.ServiceProvider.GetRequiredService<AppDbContext>();
    db.Database.Migrate();
}
await DatabaseSeeder.SeedAsync(app.Services);

// ?? Middleware pipeline ???????????????????????????????????????????????????????
if (!app.Environment.IsDevelopment())
{
    app.UseExceptionHandler("/Error");
    app.UseHsts();
    app.UseHttpsRedirection();
}

// Static files BEFORE tenant middleware — prevents DB hit on every asset request
app.UseStaticFiles();
app.UseRouting();
app.UseRateLimiter();
app.UseAuthentication();
app.UseMiddleware<TenantMiddleware>();
app.UseAuthorization();

// ?? Hangfire Dashboard (admin-only) ???????????????????????????????????????????
app.UseHangfireDashboard("/hangfire", new DashboardOptions
{
    Authorization = new[] { new HangfireAuthFilter() }
});

// ?? Register recurring Hangfire jobs ?????????????????????????????????????????
ScheduledJobService.RegisterRecurringJobs();

// ?? API endpoints ?????????????????????????????????????????????????????????????
app.MapAuthEndpoints();
app.MapLeadsEndpoints();
app.MapCallsEndpoints();
app.MapCampaignsEndpoints();
app.MapAiEndpoints();
app.MapAiInsightsEndpoints();
app.MapDashboardEndpoints();
app.MapAdminEndpoints();
app.MapSuperAdminEndpoints();
app.MapFollowUpEndpoints();
app.MapTaskEndpoints();
app.MapTimelineEndpoints();
app.MapNotificationEndpoints();
app.MapReportsEndpoints();
app.MapExportEndpoints();
app.MapWhatsAppEndpoints();
app.MapSmsEndpoints();
app.MapEmailEndpoints();
app.MapDocumentEndpoints();
app.MapWebhookEndpoints();
app.MapIntegrationsEndpoints();
app.MapCallControlEndpoints();
app.MapMeetingEndpoints();
app.MapEscalationEndpoints();
app.MapPaymentEndpoints();
app.MapRazorpayWebhookEndpoints();
app.MapDncEndpoints();
app.MapMessageTemplateEndpoints();
app.MapAgentGoalEndpoints();

// ?? SignalR + Health + Razor Pages ????????????????????????????????????????????
app.MapHub<CrmHub>("/hubs/crm");
app.MapHealthChecks("/health");
app.MapRazorPages();

app.Run();
